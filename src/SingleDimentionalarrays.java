
public class SingleDimentionalarrays {

	public static void main(String[] args) {
		
		int a[] = new int[3];
		a[0] = 1;
		a[1] = 2;
		a[2] = 3;
		for (int i = 0; i<a.length;i++) {
			System.out.println(a[i]+" ");
			
		}
		String b[] = {"Hi","Hello","How are you"};
		for (int j = 0; j < b.length;j++) {
			System.out.print(b[j]+" ");
		}
		
		

	}

}
